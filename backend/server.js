// –†–µ–≥–∏—Å—Ç—Ä–∞—Ü–∏—è
app.post('/api/auth/register', async (req, res) => {
  const client = await pool.connect();
  
  try {
    const { username, email, password } = req.body;
    
    console.log('üìù Registration attempt:', { username, email });

    // –í–∞–ª–∏–¥–∞—Ü–∏—è –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
    if (!username || !email || !password) {
      return res.status(400).json({ 
        error: 'All fields are required',
        required: ['username', 'email', 'password']
      });
    }

    if (username.length < 3) {
      return res.status(400).json({ 
        error: 'Username must be at least 3 characters long' 
      });
    }

    if (password.length < 6) {
      return res.status(400).json({ 
        error: 'Password must be at least 6 characters long' 
      });
    }

    // Email validation
    const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
    if (!emailRegex.test(email)) {
      return res.status(400).json({ 
        error: 'Please enter a valid email address' 
      });
    }

    await client.query('BEGIN');

    // –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞ —Å—É—â–µ—Å—Ç–≤—É—é—â–µ–≥–æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
    const existingUser = await client.query(
      'SELECT id, username, email FROM users WHERE username = $1 OR email = $2',
      [username.toLowerCase(), email.toLowerCase()]
    );

    if (existingUser.rows.length > 0) {
      const existing = existingUser.rows[0];
      const field = existing.username.toLowerCase() === username.toLowerCase() ? 'username' : 'email';
      return res.status(400).json({ 
        error: `This ${field} is already taken`
      });
    }

    // –•—ç—à–∏—Ä–æ–≤–∞–Ω–∏–µ –ø–∞—Ä–æ–ª—è
    const saltRounds = 12;
    const passwordHash = await bcrypt.hash(password, saltRounds);

    // –°–æ–∑–¥–∞–Ω–∏–µ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
    const result = await client.query(
      `INSERT INTO users (username, email, password_hash, balance) 
       VALUES ($1, $2, $3, $4) 
       RETURNING id, username, email, balance, created_at`,
      [username.toLowerCase(), email.toLowerCase(), passwordHash, 1000.00]
    );

    const user = result.rows[0];

    // –°–æ–∑–¥–∞–Ω–∏–µ JWT —Ç–æ–∫–µ–Ω–∞
    const token = jwt.sign(
      { 
        userId: user.id, 
        username: user.username,
        email: user.email 
      },
      process.env.JWT_SECRET || 'fallback-secret',
      { expiresIn: '7d' }
    );

    await client.query('COMMIT');

    console.log('‚úÖ User registered successfully:', user.username);

    res.status(201).json({
      success: true,
      message: 'Registration successful',
      token,
      user: {
        id: user.id,
        username: user.username,
        email: user.email,
        balance: parseFloat(user.balance),
        joinDate: user.created_at
      }
    });

  } catch (error) {
    await client.query('ROLLBACK');
    console.error('‚ùå Registration error:', error);
    
    res.status(500).json({ 
      error: 'Registration failed',
      message: 'Internal server error occurred',
      details: process.env.NODE_ENV === 'development' ? error.message : undefined
    });
  } finally {
    client.release();
  }
});

